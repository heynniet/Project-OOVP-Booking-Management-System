    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    if(txtname.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this,"Name field is required");
            txtname.requestFocus();
            return;
        }
    
    if(txtemail.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this,"Email field is required");
            txtemail.requestFocus();
            return;
        }
    
    if(!isValidEmail(txtemail.getText().trim())){
            JOptionPane.showMessageDialog(this,"Please enter a valid email address");
            txtemail.requestFocus();
            return;
        }
    
    if(new String(jPasswordField1.getPassword()).trim().equals("")){
            JOptionPane.showMessageDialog(this,"Password field is required");
            jPasswordField1.requestFocus();
            return;
        }
    
    if(!jRadioButton2.isSelected() && !jRadioButton3.isSelected()){
            JOptionPane.showMessageDialog(this,"Please select gender");
            return;
        }
    
    // Get selected gender
        String gender = jRadioButton3.isSelected() ? "Male" : "Female";
        
        PreparedStatement pst = null;
        ResultSet rs = null;
        Connection con = null;
        
    try {
            con = DBConnection.getConnection();
            
            // Check if email already exists
            pst = con.prepareStatement("SELECT * FROM users WHERE email = ?");
            pst.setString(1, txtemail.getText().toLowerCase().trim());
            rs = pst.executeQuery();
            
            if(rs.next()){
                JOptionPane.showMessageDialog(this,"This email is already registered. Please use another email ID");
                txtemail.requestFocus();
            } else {
                // Insert new user
                pst = con.prepareStatement("INSERT INTO users(name, email, password, gender) VALUES(?, ?, ?, ?)");
                pst.setString(1, txtname.getText().trim());
                pst.setString(2, txtemail.getText().toLowerCase().trim());
                pst.setString(3, new String(jPasswordField1.getPassword()));
                pst.setString(4, gender);
                
                int result = pst.executeUpdate();
                if(result > 0) {
                    JOptionPane.showMessageDialog(this, "Registration Successful!\nYou can now login with your credentials.");
                    clearFields();
                    new SignIn().setVisible(true);
                    this.dispose(); // Close signup window
                }
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database Error: " + ex.getMessage());
            ex.printStackTrace();
        } finally {
            // Close resources
            try {
                if(rs != null) rs.close();
                if(pst != null) pst.close();
                if(con != null) con.close();
            } catch(SQLException ex) {
                ex.printStackTrace();
            }
        }                                                            
    }//GEN-LAST:event_jButton1ActionPerformed

